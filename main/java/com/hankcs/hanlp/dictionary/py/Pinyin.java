    /**
     * 构造一个拼音
     * @param shengmu 声母
     * @param yunmu 韵母
     * @param tone 音调
     * @param pinyinWithToneMark 包含音调的字符串形式
     * @param pinyinWithoutTone 不含音调的字符串形式
     */
    Pinyin(Shengmu shengmu, Yunmu yunmu, int tone, String pinyinWithToneMark, String pinyinWithoutTone)
    {
        this.shengmu = shengmu;
        this.yunmu = yunmu;
        this.tone = tone;
        this.pinyinWithToneMark = pinyinWithToneMark;
        this.pinyinWithoutTone = pinyinWithoutTone;
    }

